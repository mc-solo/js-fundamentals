// boolean types

let javaScript = true;
console.log(javaScript);

console.log(typeof javaScript); //to check the data type of a var

// dynamic typing
javaScript = "Yes";
console.log(javaScript);
console.log(typeof javaScript);

// undefined data types
let varName;
console.log(varName);
console.log(typeof varName);

// null type
let year = null;
console.log(year);
console.log(typeof year); //js bug, left for legacy reasons
